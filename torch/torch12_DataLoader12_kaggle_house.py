def train(model, criterion, optimizer, loader):
    model.train()
    
    total_loss = 0
    for x_batch, y_batch in loader:
        optimizer.zero_grad()
        hypothesis = model(x_batch)
        loss = criterion(hypothesis, y_batch)

        loss.backward()
        optimizer.step()
        total_loss += loss.item()
    
    return total_loss / len(loader)

